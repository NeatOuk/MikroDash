/**
 * Returns the category string for a known org, or 'other' if unmapped.
 */
function lookupCategory(org) {
  return (org && ORG_CATEGORY[org]) || 'other';
}

// Pre-parse all CIDR ranges once at startup
const PARSED = ORGS.map(entry => ({
  org: entry.org,
  ranges: entry.ranges.map(cidr => {
    try {
      const [addr, prefix] = cidr.split('/');
      return [ipaddr.parse(addr), parseInt(prefix, 10), cidr.includes(':') ? 'v6' : 'v4'];
    } catch (_) { return null; }
  }).filter(Boolean),
}));

// LRU-style cache — cap at 5000 entries
const cache = new Map();
const CACHE_MAX = 5000;

/**
 * Look up a known org/service for an IP string.
 * Returns the org name string, or null if not matched.
 */
function lookupOrg(ip) {
  if (!ip) return null;
  if (cache.has(ip)) return cache.get(ip);

  let parsed;
  try { parsed = ipaddr.parse(ip); } catch (_) {
    cache.set(ip, null);
    return null;
  }

  const isV6 = parsed.kind() === 'ipv6';

  // IPv4-mapped IPv6 (::ffff:x.x.x.x) — unwrap to v4 for matching
  let v4mapped = null;
  if (isV6) {
    try { v4mapped = parsed.toIPv4Address(); } catch (_) {}
  }

  let result = null;
  outer: for (const entry of PARSED) {
    for (const [net, prefix, ver] of entry.ranges) {
      try {
        if (ver === 'v4' && !isV6  && parsed.match([net, prefix])) { result = entry.org; break outer; }
        if (ver === 'v6' && isV6   && parsed.match([net, prefix])) { result = entry.org; break outer; }
        if (ver === 'v4' && v4mapped && v4mapped.match([net, prefix])) { result = entry.org; break outer; }
      } catch (_) {}
    }
  }

  if (cache.size >= CACHE_MAX) {
    // Evict oldest entry
    cache.delete(cache.keys().next().value);
  }
  cache.set(ip, result);
  return result;
}

module.exports = { lookupOrg, lookupCategory };
